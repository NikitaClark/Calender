package cs3500.pa05.model;

/**
 * Represents the days of the week
 */
public enum DaysOfWeek {

  /**
   * Represents Sunday
   */
  SUNDAY,

  /**
   * Represents Monday
   */
  MONDAY,

  /**
   * Represents Tuesday
   */
  TUESDAY,

  /**
   * Represents Wednesday
   */
  WEDNESDAY,

  /**
   * Represents Thursday
   */
  THURSDAY,

  /**
   * Represents Friday
   */
  FRIDAY,

  /**
   * Represents Saturday
   */
  SATURDAY;


}
